# Função Somar recebendo dois parâmetros.
def soma(number1, number2):
    print(f'Numero 1 {number1} + Numero 2 {number2}')
    soma_ = number2 + number1
    print(f'A soma é: {soma_}')
    print()


# Programa principal
soma(4, 2)
soma(3, 5)
soma(7, 1)
