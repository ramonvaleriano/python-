# Função titulo para mostrar mensagem.
def titulo(mensagem):
    print('='*50)
    print(f'{mensagem:^50}')
    print('='*50)

# Programa Principal
titulo('Curso em Video.')
titulo('Aprenda Python.')
titulo('Ramon R. Valeriano.')